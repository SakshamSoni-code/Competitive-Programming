#include<bits/stdc++.h>
using namespace std;
const int N=1e6;
int ar[N+1];

void seive()
 {
    for(int i=2;i<=N;i++)
    {
        if(ar[i]==0)
        {
            for(int j=i;j<=N;j+=i)
            {
                if(ar[j]==0)
                {
                    ar[j]=i;
                }
            }
        }
    }
 }



int main()
{
    
    ios_base::sync_with_stdio(false); 
    cin.tie(nullptr); cout.tie(nullptr);
    
    ll L,R;
    cin>>L;
    seive();
    while(L--)
    {
        cin>>R;
        cout<<R-ar[R]<<"\n";
    }
     return 0;   
}






